# Функции для работы с вопросами
def get_all_questions():
    """Возвращает все вопросы"""
    return RUSSIAN_QUESTIONS


def get_questions_by_difficulty(difficulty='all'):
    """Возвращает вопросы по уровню сложности"""
    if difficulty == 'all':
        return RUSSIAN_QUESTIONS
    return [q for q in RUSSIAN_QUESTIONS if q['difficulty'] == difficulty]


def get_questions_by_category(category='all'):
    """Возвращает вопросы по категории"""
    if category == 'all':
        return RUSSIAN_QUESTIONS
    return [q for q in RUSSIAN_QUESTIONS if q['category'] == category]


def get_random_questions(count=10, difficulty='all'):
    """Возвращает случайные вопросы"""
    import random
    if difficulty == 'all':
        questions = RUSSIAN_QUESTIONS
    else:
        questions = get_questions_by_difficulty(difficulty)

    return random.sample(questions, min(count, len(questions)))


def get_question_by_id(question_id):
    """Находит вопрос по ID"""
    for question in RUSSIAN_QUESTIONS:
        if question['id'] == question_id:
            return question
    return None