# Функции для работы с вопросами (аналогично math_questions.py)
def get_all_history_questions():
    return HISTORY_QUESTIONS

def get_history_question_by_id(question_id):
    for question in HISTORY_QUESTIONS:
        if question['id'] == question_id:
            return question
    return None

def get_history_questions_by_difficulty(difficulty='all'):
    if difficulty == 'all':
        return HISTORY_QUESTIONS
    return [q for q in HISTORY_QUESTIONS if q['difficulty'] == difficulty]

def get_history_questions_by_category(category='all'):
    if category == 'all':
        return HISTORY_QUESTIONS
    return [q for q in HISTORY_QUESTIONS if q['category'] == category]

def get_random_history_questions(count=10, difficulty='all'):
    import random
    if difficulty == 'all':
        questions = HISTORY_QUESTIONS
    else:
        questions = get_history_questions_by_difficulty(difficulty)
    return random.sample(questions, min(count, len(questions)))